// Write three functions
// add, sub and mul
// These functions take two arguments and return their
// sum, difference and product, such as:

add(3, 4) //  7
sub(3, 4) // -1
mul(3, 4) // 12


function add(first, second){
  return first + second
}

function sub(first, second){
  return first - second
}

function mul(first, second){
  return first * second;
}